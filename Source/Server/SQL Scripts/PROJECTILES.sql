IF NOT EXISTS(SELECT * FROM INFORMATION_SCHEMA.TABLES WHERE TABLE_SCHEMA = 'dbo' AND TABLE_NAME = 'PROJECTILES')
CREATE TABLE PROJECTILES (
	ID INT IDENTITY(1, 1) PRIMARY KEY,
	NAME VARCHAR(25) NOT NULL,
	DAMAGE INTEGER,
	RANGE INTEGER,
	SPRITE INTEGER,
	TYPE INTEGER,
	SPEED INTEGER
	)

SELECT ID,
	Owner,
	Slot,
	Name,
	Sprite,
	Damage,
	Armor,
	Type,
	Attack_Speed,
	Health_Restore,
	Mana_Restore,
	Strength,
	Agility,
	Intelligence,
	Energy,
	Stamina,
	Value,
	Price,
	Rarity,
	CoolDown,
	AddMax_Health,
	AddMax_Mana,
	Bonus_XP,
	Spell_Number,
	Stackable,
	MaxStack
FROM Equipment
WHERE Owner = @owner
	AND Slot = @slot
